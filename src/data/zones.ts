// ── Helpers ───────────────────────────────────────────────────────────────────

export function getGrowingZones(): ZoneTemplate[] {
  return zoneTemplates.filter((z) => z.category === 'growing');
}

export function getStructureZones(): ZoneTemplate[] {
  return zoneTemplates.filter((z) => z.category === 'structure');
}

export function getZoneTemplate(type: ZoneType): ZoneTemplate | undefined {
  return zoneTemplates.find((z) => z.type === type);
}
